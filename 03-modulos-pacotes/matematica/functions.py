def media(valores: list):
    """Calcula media dos valores de uma lista

    Args:
        valores (list): lista de notas

    Returns:
        number: media das notas
    """

    return sum(valores) / len(valores)


def somar(n1, n2):
    """Calcula a soma de dois número

    Args:
        n1 (number): primeiro número
        n2 (number): segundo número

    Returns:
        number: soma dos números
    """
    return (n1 + n2)


def multiplicar(n1, n2):
    return n1 * n2